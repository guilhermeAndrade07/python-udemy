# Repetições com for e While

# FOR --> Utilizar quando eu sei a quantidade de vezes que preciso repetir
for i2 in range(11):
    print(i2)

print('-------------')

for i in range(11): 
    print(i * 0.5)

print('-------------')

for i3 in range(10, 0, -2):
    print(i3)

print('-------------')

for i4 in range(10, 0, -1):
    print(i4)

print('-------------')

nomes = ['Guilhere', 'Joquim', 'Gobato']
for saudacao in nomes:
    print('Olá', saudacao)